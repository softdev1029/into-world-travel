<h1 style="color:#8e7344;font-size:28px;font-family:helvetica,arial;padding:4px 0;font-weight:bold;text-transform:none;line-height:30px;margin-bottom:0;">Russian Transit Visa</h1>
<p>The Russian Transit Visa is suitable for travellers that travel to a third country via Russia. This type 

of Visa is normally valid for 3 days if travelling by air and 10 days if you are travelling by train.

 Transit Visa is not required if you remain in the transit zone of the airport in order to catch an 

onward or connecting flight within 24 hours of arrival. 

Please check with your travel agent or airline whether you will be staying in the same 

terminal/transit zone in order to catch a connecting flight before you decide whether transit visa is 

required or not.

Passengers travelling to and from Belarus via Russian Federation need to obtain the transit 

Russian visa irrespectively of their length of stay in the airport.</p>

<p><b>Please note from 10th of December 2014 each applicant needs to apply in person for New Russian 

Biometric Visa. IntoRussia still offers a range of services to assist you in obtaining one.</b></p>

<p><strong>Visa Requirements:</strong></p>
