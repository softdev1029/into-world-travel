<h1 style="color:#8e7344;font-size:28px;font-family:helvetica,arial;padding:4px 0;font-weight:bold;text-transform:none;line-height:30px;margin-bottom:0;">Russian Private Visa</h1>
<p>The Russian Private Visa is suitable for travellers who have relatives or friends in Russia and are 

able to get a private invitation. Private visa can be valid for up to 90 days and can be issued for 

single or double entry. Dates of validity of the visa will correspond with the dates mentioned on your 

private invitation from relatives/friend in Russia.</p>

<p><b>Please note from 10th of December 2014 each applicant needs to apply in person for New Russian 

Biometric Visa. IntoRussia still offers a range of services to assist you in obtaining one.</b></p>

<p><strong>Visa Requirements:</strong></p>
<ul>
  <li>Original Passport, with at least 2 blank pages and valid for more than 6 months after visa expiration date</li>
  <li><a href="https://visa.kdmid.ru/" target="_blank">Application Form</a></li>
  <li><a href="#" onclick="document.getElementById('b54').style.display='block';return false;">One passport sized Photograph</a></li>
