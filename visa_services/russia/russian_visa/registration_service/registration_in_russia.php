<h1 style="color:#8e7344;font-size:28px;font-family:helvetica,arial;padding:4px 0;font-weight:bold;text-transform:none;line-height:30px;margin-bottom:0;">Russian Registration Service</h1>

<p>When travelling to Russia, it is important to remember that it has special <strong>Traveller Registration policies</strong>.</p>
<p>After you enter Russia, you must acquire <strong>Registration of Foreigners paperwork</strong> at an OVIR office (Office of Visas and Registration). Getting a Russian registration certificate within the first 7 working days of your trip to Russia is a legal requirement. If you stay less than 7 business days you do not need to register. Filling out an Immigration card at border control is not enough to register in Russia, but an OVIR's registration officer will require your Immigration card in order to register you.</p>

<p>Travellers who stay in hotels do not have to register because the hotel will carry out the registration process on their behalf. However any business, private or tourist traveller that is staying in private accommodation, for example within their own apartment, with friends or being hosted by business partners, <em>must</em> register in Russia by themselves.</p>

<p>The penalties for failing to obtain a Russian registration certificate include possible deportation and a visa blacklisting for future trips. Lack of Russian visa registration can also prevent you from taking part in many normal tourist activities, such as renting a car or buying a SIM card.</p>

<h2>How to register in Russia through IntoRussia</h2>
<p>If you have not booked your <strong>Russian visa</strong> with us, you can still register in Russia with us by providing a copy of the first page of your passport and a copy of your Russian Visa. Send it to us via post or email before you start your <strong>trip to Russia</strong> and then follow the instructions below.</p>

<ul>
<li>After passing passport control where your Immigration card will be stamped, take a photo with your mobile phone of the Immigration card and send the image via MMS or email to IntoRussia.</li>
<li>IntoRussia will complete the Russian registration form for you in OVIR and send you the completed Russian registration certificate/Registration of Foreigners back via email.</li>
<li>Print out the <strong>Russian registration certificate</strong> and keep it with you at all times.</li>
</ul>

<p><b>Please note that it takes up to 2-3 working days to register you.</b></p>

