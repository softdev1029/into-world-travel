<h2>Documents Required</h2>
<ol>
<li>2 copies of the visa application form. You must complete and sign all fields otherwise your application will be rejected.</li>
<li>Copy of the letter of invitation from inviting partners in Uzbekistan except for citizens of the Great Britain, USA, Austria, Belgium, Germany, Italy, France, Japan, Spain and Switzerland. Business single and multiple entry visas are issued upon the confirmation received by the Embassy of Uzbekistan from the Ministry of Foreign Affairs of the Republic of Uzbekistan. Your inviting partners should submit an Invitation letter (Notification) issued by the Immigration Department of the Ministry of Internal Affairs of Uzbekistan to the Consular Department of the Ministry of Foreign Affairs of Uzbekistan in Tashkent.</li>
<li>Passport must have at least one blank page to affix the visa and be valid for the entire period of your stay in the country.</li>
<li>Copy of all used passport pages that contain stamps and visas.</li>
<li>Passport must be valid for at least 6 months after the date of expiry of the visa.</li>
</ol>

<h2>Additional Information</h2>
<p>If your stay in Uzbekistan exceeds 3 days, you are required to register with the Local department of the Ministry of Internal Affairs within 3 working days after arrival. However if you stay in a hotel, the hotel should take care of such registration on your behalf automatically.</p>
