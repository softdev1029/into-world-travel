<h2>Documents Required</h2>
<ol>
<li>2 copies of the visa application form. You must complete and sign all fields otherwise your application will be rejected.</li>
<li>Copy of the invitation from inviting partners in Uzbekistan or copy of the air ticket to your final destination.</li>
<li>2 recent colour passport sized photos.</li>
<li>Passport must have at least one blank page to affix the visa and be valid for the entire period of your stay in the country.</li>
<li>Copy of all used passport pages that contain stamps and visas.</li>
<li>Passport must be valid for at least 6 months after the date of expiry of the visa.</li>
</ol>

<h2>Additional Information</h2>
<p>If your stay in Uzbekistan exceeds 3 days, you are required to register with the Local department of the Ministry of Internal Affairs within 3 working days after arrival. However if you stay in a hotel, the hotel should take care of such registration on your behalf.</p>
