<h2>*Letter of Invitation</h2>
<h3>For Short-term business visa (up to 90 days for single, double, multiple entries):</h3>
<p>The invitation should:</p>
<ul>
<li>be printed on the official letterhead with a registration number, date of issue, the full name of the company, its location and telephone number</li>
<li>bear the signature of the official and a corporate seal</li>
<li>indicate the expected duration of the applicant's stay in Belarus</li>
<li>state the inviting partyâ€™s written guarantees that the invitee will abide by the rules and regulations applied to foreign citizens in the Republic of Belarus</li>
</ul>


<h3>For Long-term business visa (multiple entry valid for 1 year with the right to stay up to 90 days within a year) the following documents will be required:</h3>
<ul>
	<li>An original copy of your "Hodatajstvo", or letter of invitation (<a href="Hodatajstvo.doc" target="_blank">click here for an example</a>), in Russian from a Belarusian company on official headed paper signed by a company executive and sealed by a corporate seal.</li>
	<li>A certified copy of a proof of registration of a Belarusian company (extract from the State Register of Legal Entities and Private Entrepreneurs of Belarus issued by the Ministry of Justice of Belarus)</li>
	<li>A certified copy of the contract between Belarusian company and the applicant or the company where he/she works</li>
	<li>An original document certifying that the applicant is employed by that company.</li>
</ul>
<p>Journalists can apply for this type of visa only after being accredited by the Ministry of Foreign Affairs of the Republic of Belarus.</p>

<h2>Processing Your Visa Application</h2>
<p>Please <a href="http://www.mfa.gov.by/docs/visa_forms/english.pdf" target="_blank">click here</a> to fill in the <strong>Belarusian visa application form</strong>. Then, forward these documents to IntoRussia along with any additional supporting documents and the relevant fees.</p>
<p>All applications will be lodged at the Consulate the day following delivery to our office. Normally, your passport will be sent to you by Royal Mail Special Delivery (Postage is an additional &pound;8.50 for delivery by 1pm Monday to Friday, or &pound;15.00 for delivery by 9am Monday to Saturday.) or you can enclose a pre-paid self-addressed envelope. If you are collecting in person, you will be able to have your passport on the next day after we receive it from the Belarus Consulate.</p>
 
<h2>Registration in Belarus</h2>
<p>Within 5 business days after arrival to Belarus visitors are required to visit the local police office and report information regarding their current address and duration of stay. Thereafter, local police authorities will issue a registration stamp, which will be attached to the visitor's passport. If the visitor is staying in a hotel, then this will be arranged by the hotel services (reception bureau).</p>

