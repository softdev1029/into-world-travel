<h2>*Letter of Invitation</h2>
<h3>For Short-term Visitor Visa (up to 90 days for single, double, multiple entries):</h3>
<p>If you intend to stay in the Republic of Belarus for the period less than 30 days  a letter of invitation is not required. Still, the inviting person (Belarusian citizen) or organization and their address must be indicated in the visa application form. Copy of a Belarusian citizen passport (inviting person) must be provided. This is only for the citizens of migration-secure states.</p>

<p>If you intend to stay for a period of up to 90 days ONE of the following visa support documents:</p>
<ul>
<li>an original invitation issued by the local office of Citizenship and Migration Department of the Ministry of Internal Affairs of the Republic of Belarus upon request of the applicant’s relative/friend</li>
<li><a href="BelarusInvitation.rtf" target="_blank">an original invitation completed by a Belarus national</a> whose signature is confirmed by a notary public of the Republic of Belarus or by the Consular officer at the Embassy of Belarus in the UK – if the Belarus national is on a temporary visit to the United Kingdom (the invitation is valid for 90 days from the date of notarial certification)</li>
<li>an original document confirming the ownership of the real estate property in Belarus – for obtaining a visa for the owner of that property and their family members</li>
<li>a subpoena, including the one received by facsimile or other means of communication official confirmation (such as physician’s certified report or death certificate) of serious illness or death of the applicant’s close relative or spouse – for processing a visa to a foreign national, as well as to their close relatives and family members, whose close relative or spouse became seriously ill or died in Belarus</li>
<li>a written request from a Belarusian healthcare institution or a contract for health (medical) services with Belarusian healthcare institution issued in accordance with Belarusian legislation – for processing a visa to a foreign national who needs to receive medical care in Belarus as well as to their escort person, if required</li>
</ul>

<h3>For long-term Visitor Visa (multiple entries, valid for 1 year with right to stay up to 90 days within a year):</h3>
<p>When applying for this type of visa an applicant needs to submit ONE of the following visa support documents:</p>
<ul>
<li>an original invitation issued by the local office of Citizenship and Migration Department of the Ministry of Internal Affairs of the Republic of Belarus upon request of the applicant’s close relative</li>
<li><a href="BelarusInvitation.rtf" target="_blank">an original invitation completed by a Belarus national</a> (a spouse or a close relative of the applicant) whose signature is confirmed by the Consular officer at the Visa Section of the Embassy of Belarus in the UK – if the Belarus national is on a temporary visit to the United Kingdom. The invitation is valid for 90 days from the date of notarial certification</li>
<li>an original document confirming the ownership of the real estate property in Belarus – for obtaining a visa for the owner of that property and their family members</li>
</ul>

<h2>Processing Your Visa Application</h2>
<p>Please <a href="http://www.mfa.gov.by/docs/visa_forms/english.pdf" target="_blank">click here</a> to fill in the <strong>Belarusian visa application form</strong>. Then, forward these documents to IntoRussia along with any additional supporting documents and the relevant fees.</p>
<p>All applications will be lodged at the Consulate the day following delivery to our office. Normally, your passport will be sent to you by Royal Mail Special Delivery (Postage is an additional &pound;8.50 for delivery by 1pm Monday to Friday, or &pound;15.00 for delivery by 9am Monday to Saturday.) or you can enclose a pre-paid self-addressed envelope. If you are collecting in person, you will be able to have your passport on the next day after we receive it from the Belarus Consulate.</p>
 
<h2>Registration in Belarus</h2>
<p>Within 5 business days after arrival in Belarus visitors are required to visit the local police office and report information regarding their current address and duration of stay. Thereafter, local police authorities will issue a registration stamp, which will be attached to the visitor's passport. If the visitor is staying in a hotel, then this will be arranged by the hotel services (reception bureau).</p>

