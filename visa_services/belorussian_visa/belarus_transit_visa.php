<h1 style="color:#8e7344;font-size:28px;font-family:helvetica,arial;padding:4px 0;font-weight:bold;text-transform:none;line-height:30px;margin-bottom:0;">Belarus Transit Visa</h1>

<p>All visitors to Belarus require a <strong>valid Belarusian Visa</strong>. 
The granting of a visa to enter Belarus is a legal requirement at the discretion of the Belarus Consulate. All information on this page pertains specifically to Transit visas, which are valid up to a maximum of 48 hours from the time of entry into the territory of Belarus, however other types of visa are available. Please make your selection from the left-hand menu or contact our Visa Enquiry Office directly for more information.</p>

<h2>Documents Required for Visa Application</h2>
<p>To process the <strong>Belarus Visa Application</strong>, we will require the following documents from you: </p>
<ul>
	<li>The visa application form should be completed in black ink. If a question does not apply, please write "n/a". Please type or print clearly and remember to sign the form. Please ensure the signature is identical to the one on your passport. If the form is completed incorrectly the application will be rejected.</li>
	<li>A valid visa for the country of final destination (e.g. Russian visa). If such a visa is not required; copy of airplane, bus or train tickets, or booking confirmation plus itinerary from a travel agency.</li>
	<li>A valid passport which should have at least two blank pages for the visa. A passport must be valid at least 3 months after your exit from Belarus.</li>
	<li>One recent, colour passport-sized photo.</li>
</ul>

