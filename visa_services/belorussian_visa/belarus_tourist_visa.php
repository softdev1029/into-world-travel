<h1 style="color:#8e7344;font-size:28px;font-family:helvetica,arial;padding:4px 0;font-weight:bold;text-transform:none;line-height:30px;margin-bottom:0;">Belarus Tourist Visa</h1>

<p>All visitors to Belarus require a <strong>valid Belarusian Visa</strong>. 
The granting of a visa to enter Belarus is a legal requirement at the discretion of the Belarus Consulate. All information on this page pertains specifically to Tourist visas, however other types of visa are available. Please make your selection from the left-hand menu or contact our Visa Enquiry Office directly for more information.</p>

<h2>Documents Required for Visa Application</h2>
<p>To process the <strong>Belarus Visa Application</strong>, we will require the following documents from you: </p>
<ul>
	<li>The <a href="http://www.mfa.gov.by/docs/visa_forms/english.pdf" target="_blank">visa application form</a> should be completed in black ink. If a question does not apply, please write "n/a". Please type or print clearly and remember to sign the form. Please ensure the signature is identical to the one on your passport. If the form is completed incorrectly the application will be rejected.</li>
	<li>A "hodatajstvo", or letter of invitation (<a href="Hodatajstvo.doc" target="_blank">click here for an example</a>), issued by a Belarus tourist company with the full name of the tourist organization, its location and telephone number, purpose of visit and period of stay of a foreign citizen, commitment of the tourist company that the foreign will comply with the rules of stay in Belarus, sealed by the company.  We can provide this invitation for you. Please note that the prices and conditions of services for Belarusian tourist visa invitations (hodatajstvo) are not valid for countries within Asia, Africa and Latin America. Please contact us directly for more specific information regarding these countries.</li>
	<li>Medical insurance (<a href="medical-insurance.php">click here for more information</a>)</li>
	<li>A valid passport which should have at least two blank pages for the visa. A passport must be valid at least 3 months after your exit from Belarus.</li>
	<li>One recent, colour passport-sized photo.</li>
	<li>If the applicant previously had the USSR or Belarusian citizenship, it is necessary to submit the document proving the loss of the USSR or Belarusian citizenship.</li>
	<li>If the applicant is a child under 18 years of age, a copy of the child's birth certificate and copies of parents' passport main information pages (with passport number, expiration date, photo etc.) have to be submitted.</li>
</ul>

