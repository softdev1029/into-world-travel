<p>N.B. A double-entry or multiple-entry tourist visa can only be issued on the basis of the invitation which states the reasons for more than one entry (a visit to the neighbouring states during the trip, air/bus/train etc. tickets, visa, an appropriate tour program etc).</p>

<h2>Processing Your Visa Application</h2>
<p>Please <a href="http://www.mfa.gov.by/docs/visa_forms/english.pdf" target="_blank">click here</a> to fill in the <strong>Belarusian visa application form</strong>. Then, forward these documents to IntoRussia along with any additional supporting documents and the relevant fees.</p>
<p>All applications will be lodged at the Consulate the day following delivery to our office. 
Normally, your passport will be sent to you by Royal Mail Special Delivery (Postage is an additional &pound;8.50 for delivery by 1pm Monday to Friday, or &pound;15.00 for delivery by 9am Monday to Saturday) or you can enclose a pre-paid self-addressed envelope. If you are collecting in person, you will be able to have your passport on the next day after we receive it from the Belarus Consulate.</p>
 
<h2>Registration in Belarus</h2>
<p>Within 5 business days after arrival to Belarus visitors are required to visit the local police office and report information regarding their current address and duration of stay. Thereafter, local police authorities will issue a registration stamp, which will be attached to the visitor's passport. If the visitor is staying in a hotel, then this will be arranged by the hotel services (reception bureau).</p>

