<h1 style="color:#8e7344;font-size:28px;font-family:helvetica,arial;padding:4px 0;font-weight:bold;text-transform:none;line-height:30px;margin-bottom:0;">Medical Insurance for Belarus Visa</h1>

<p>On July 1, 2014 the new procedure and conditions of the obligatory medical insurance of foreign citizens and stateless persons travelling except UK PASSPORT HOLDERS to the Republic of Belarus and temporarily residing in the Republic of Belarus will be introduced.</p>

<p>The visa applicants should submit the obligatory medical insurance policy of the Belarusian enterprises “Belgosstrakh” or “Beleximgarant”, foreign medical insurance policy, medical insurance card or other document that confirms its existence.</p>

<p>The foreign medical insurance policy or document that confirms its existence can be presented in original or copy and should contain the following information:</p>
<ul>
<li>the name and address of the foreign insurance company, phone number of the foreign insurance company or of the international assistance company, the full name of the foreign citizen</li>
<li>the territorial validity should include the Republic of Belarus</li>
<li>it should be valid for the period of the travel or temporary residence in the Republic of Belarus</li>
<li>the minimum limit of responsibility of the medical insurance is 10 000 Euro</li>
</ul>
 
<p>The medical insurance is not required for foreigners who travel to other countries via Belarus by train or by air in trains of the international routes and planes of the civic international air companies.</p>

