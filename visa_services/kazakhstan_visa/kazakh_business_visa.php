<h2>*Letter of Invitation</h2>
<p>The citizens of the following countries do not require an official invitation for a business  visa (30 days):</p>
<p style="font-size:10px;">Australia, Austria, Belgium, Brazil, Bulgaria, Canada , Croatia, Czech Republic, Cyprus, Denmark, Finland, France, Germany, Greece, Hungary, Israel, Italy, Ireland, Iceland, Japan, Liechtenstein, Luxemburg, Latvia, Lithuania, Malta, Malaysia,  Monaco, the Netherlands, New Zealand, Norway, Poland, Portugal, Romania, Saudi Arabia, Singapore, Slovakia, Slovenia, South Korea, Spain, Sweden, Switzerland,  the Sultanate of Oman, the United Kingdom of Great Britain and Northern Ireland, USA, United Arab Emirates</p>
<p>Double and multiple-entry applications require a telex from the Consular Department of the Ministry of Foreign Affairs in Kazakhstan. Once the London Consulate has been sent details of your invitation from the authorities in Kazakhstan they will be in a position to process your application.</p>

<h2>Additional Information</h2>
<p>If you are applying for children under the age of 18, please also present the following:
	<ul>
		<li>A copy of both parents' passports</li>
		<li>A copy of the child's birth certificate</li>
		<li>A letter from one of the child's parents stating non-objection to the child's other parent taking him or her to Kazakhstan</li>
	</ul>
</p>

<p>Please note that all foreign citizens must register at the local Office of the Ministry of Internal Affairs of Kazakhstan, or at the hotel, within 72 hours of arrival to Kazakhstan. Otherwise they can be fined by the Immigration Service upon departure.</p>

