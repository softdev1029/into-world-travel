<p>To process the Kazakhstan Visa Application, we will require the following documents from you: </p>
<ul>
	<li>Completed visa application form. You must complete and sign all fields otherwise your application will be rejected.</li>
	<li>An official invitation letter (original version required).*</li>
	<li>Covering letter from your employer briefly explaining the purpose of your visit and including some info about company or organization should be attached.</li>
	<li>A valid passport that should have at least one blank page for a visa.  A passport must valid at least 6 months after a visa expire date.</li>
	<li>UK investors in the Kazakhstan economy who already have business projects in Kazakhstan may apply for multiply investor visas valid for 24 months. Investor visa holders are not required to be registered with the Immigration Department of the Ministry of Internal Affair of Kazakhstan.</li>
</ul>

