<h2>*Letter of Invitation</h2>
<p>No official invitation is required for private single-entry visa up to <u>30 days</u> if you are a citizen of one of the countries below. However, if you are intending to stay up to <u>90 days</u> the Consulate has the right to request an official private invitation arranged through the Ministry of Foreign Affairs in Kazakhstan. Your relatives or friends in Kazakhstan can obtain invitation from the local authorities of MIA and send it to your address in the UK. Please note that the period of stay in Kazakhstan must not exceed 90 days including dates of entry and departure from the country.</p>
