<h2>Additional Information</h2>
<p>Transit visa is applicable for up to 5 days for single entry only.</p>
<p>If you are applying for children under the age of 16, please also present the following:
	<ul>
		<li>A copy of both parents' passports</li>
		<li>A copy of the child's birth certificate</li>
		<li>A letter from one of the child's parents stating non-objection to the child's other parent taking him or her to Kazakhstan</li>
	</ul>
</p>

