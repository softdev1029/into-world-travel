<h1 style="color:#8e7344;font-size:28px;font-family:helvetica,arial;padding:4px 0;font-weight:bold;text-transform:none;line-height:30px;margin-bottom:0;">Kazakhstan - Transit Visa</h1>

<p>All visitors to Kazakhstan require a valid Kazakhstan Visa. 
The granting of a visa to enter Kazakhstan is a legal requirement at the discretion of the Kazakhstan Consulate. All information on this page pertains specifically to Transit visas, however we can also offer other types of visa. Please select the relevant option from the left-hand menu, or contact our Visa Enquiry Office directly for more information.</p>

<h2>Documents Required for Visa Application</h2>
<p>To process the Kazakhstan Visa Application, we will require the following documents from you: </p>
<ul>
	<li>Completed visa application form. You must complete and sign all fields otherwise your application will be rejected.</li>
	<li>A letter of request to issue a visa addressed to the Consular Section of the Embassy of Kazakhstan explaining the reason of traveling and requesting a relevant visa. It should contain dates of your trip and places to be visited. Click the following link to download a <a href="kazakh_letter_of_request.doc" target="_blank">sample letter of request</a>.</li>
	<li>Letter should be typed on computer, printed out and signed.</li>
	<li>A valid passport that should have at least one blank page for a visa.  A passport must valid at least 6 months after a visa expire date.</li>
	<li>One recent, colour  passport-sized photo.</li>
	<li>Full ticket and visa to the country of final destination.</li>
</ul>

